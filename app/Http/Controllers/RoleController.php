    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Role $role)
    {
        $role->delete();
        return back()->with('success-message', 'Role Deleted Successfully!');
    }

    public function permissionForm(Role $role)
    {
        return view('role.set-permission', ['role'=>$role, 'permissions'=>Permission::all()]);
    }
    public function setPermission(Request $request, Role $role)
    {
        if($role->permissions){
            $role->permissions()->sync($request->permission_id);
        }
        $role->permissions()->attach($request->permission_id);
        return redirect()->route('role.index')->with('success-message', 'Permission added to role');
    }
